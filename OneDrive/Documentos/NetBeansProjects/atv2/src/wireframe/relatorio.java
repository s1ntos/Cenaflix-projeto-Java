     DefaultTableModel tabelaFilmes = (DefaultTableModel) tabela.getModel();
     
     tabelaFilmes.setNumRows(0);
 
     for (Filmes c : listaFilmes) { //em cada volta do la√ßo for, o mesmo adiciona uma dado(filme) dentro do objeto c
      Object[] obj = new Object[] { 
       c.getId(), //id
       c.getNomefilme(), 
       c.getDta(), 
       c.getCategoria(), 
    };
     tabelaFilmes.addRow(obj);
   } 
